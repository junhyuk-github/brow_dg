	function handleScroll() {
		const element = document.getElementById('pgNav');
		const scrollY = window.scrollY;

		if (scrollY > 330) { //Thumbnail - gnb
			element.classList.add('scrolled');
		} else {
			element.classList.remove('scrolled');
		}
	}

