      backToTop.classList.add('show');
    }else{
      backToTop.classList.remove('show');
    }
  }
  const moveBackToTop=()=>{
    if(window.pageYOffset > 0 ){
      window.scrollTo({top:0, behavior:"smooth"});
    }
  }

  // 스크롤할때마다, checkScroll을 호출
  window.addEventListener('scroll', checkScroll);
